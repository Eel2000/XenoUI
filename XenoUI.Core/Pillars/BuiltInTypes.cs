    /// within the XenoUI framework. This is utilized to define and reference
    /// Button types consistently throughout the framework's component hierarchy.
    /// </summary>
    public const uint Button = 3;

    /// <summary>
    /// Represents a predefined built-in type identifier for a Text component
    /// within the XenoUI framework. This is utilized to define and reference
    /// Text types consistently throughout the framework's component hierarchy.
    /// </summary>
    public const uint Text = 4;

    /// <summary>
    /// Represents a predefined built-in type identifier for an Image component
    /// within the XenoUI framework. This is utilized to define and reference
    /// Image types consistently throughout the framework's component hierarchy.
    /// </summary>
    public const uint Image = 5;
}